package Operators;

public class Comparison_Operators {
public static void main(String[] args) {
	int number1=12;
	int number2=12;

	//==
	System.out.println( number1==number2);
	
	
	// !=
	number2=11;
	System.out.println( number1!=number2);
	
	// >
	 
	System.out.println( number1>number2);
	
	//<
	System.out.println(number1<number2);
	
	//>=
	System.out.println( number1>=number2);
	
	//<=
	System.out.println( number1<=number2);
	
	
}
}
