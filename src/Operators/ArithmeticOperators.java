package Operators;

public class ArithmeticOperators {
	public static void main(String[] args) {
		int number1=22;
		int number2=32;
		
		// +
		System.out.println( number1+number2);
		
		// -
		System.out.println( number1-number2);
		
		// *
		System.out.println( number1*number2);
		
		// /
		System.out.println( number1/number2);
		
		//%
		System.out.println( number1%number2);
		
		
	}

}
